//! Rust port of the MINIX/NetBSD `banner` utility.
//!
//! Usage:
//!   banner [-w width] message ...
//!
//! Prints large ASCII art banners.

use std::collections::HashMap;

fn main() {
    let args: Vec<String> = std::env::args().collect();
    let mut argv = &args[1..];
    let mut width: usize = 80;

    while !argv.is_empty() && argv[0].starts_with('-') && argv[0] != "--" {
        let opt = argv[0].clone();
        if opt == "--" { argv = &argv[1..]; break; }
        if opt.starts_with("-w") {
            if opt.len() > 2 {
                width = opt[2..].parse().unwrap_or_else(|_| { eprintln!("usage: banner [-w width] message ..."); std::process::exit(1); });
            } else {
                argv = &argv[1..];
                if argv.is_empty() { eprintln!("usage: banner [-w width] message ..."); std::process::exit(1); }
                width = argv[0].parse().unwrap_or_else(|_| { eprintln!("banner: invalid width"); std::process::exit(1); });
            }
        } else {
            eprintln!("usage: banner [-w width] message ...");
            std::process::exit(1);
        }
        argv = &argv[1..];
    }

    if argv.is_empty() {
        eprintln!("usage: banner [-w width] message ...");
        std::process::exit(1);
    }

    let text = argv.join(" ").to_uppercase();
    let font = get_font();
    let char_height = 8;
    let char_width = 8;

    for row in 0..char_height {
        let mut line = String::new();
        for ch in text.chars() {
            if let Some(glyph) = font.get(&ch) {
                if row < glyph.len() {
                    line.push_str(&glyph[row]);
                }
            }
        }
        if line.len() > width {
            line = line[..width].to_string();
        }
        println!("{}", line);
    }
}

